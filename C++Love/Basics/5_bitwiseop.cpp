#include<iostream>
using namespace std;

int main()
{
    int a = 5;
    int b = 7;
    // and, or, xor, not
    cout<<"a & b : "<< (a&b) << endl;
    cout<<"a | b : "<< (a|b) << endl;
    cout<<"a ^ b : "<< (a^b) << endl;
    cout<<"~a : "<< (~a) << endl;
    
    // Left shift and right shift
    cout<< "17 >> 1 : "<<(17>>1)<<endl;
    cout<< "17 >> 2 : "<<(17>>2)<<endl;
    cout<< "19 << 1 : "<<(19<<1)<<endl;
    cout<< "21 << 2 : "<<(21<<2)<<endl;

    

}